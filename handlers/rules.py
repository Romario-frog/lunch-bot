from aiogram import Router, F
from aiogram.types import Message

router = Router()

RULES_TEXT = '''ℹ️ Правила обідів:
• Обід завжди триває 45 хвилин.
• Час можна обирати з кроком 15 хвилин: 10:00, 10:15, 10:30, 10:45 і т.д.
• Не можна виходити за межі 20:00, тому останній старт обіду — 19:15.
• Максимум 2 людини на один і той самий період.
• Оператор може керувати тільки своїм обідом.
• Адмін бачить користувачів, логи та загальний розклад.
• Нагадування приходить в особисті повідомлення за 15 хвилин.'''


@router.message(F.text == 'ℹ️ Правила')
async def rules(message: Message):
    await message.answer(RULES_TEXT)
